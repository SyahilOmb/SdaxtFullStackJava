package fiftyprog.java;

public class twentyone 
{
	
	    public static void main(String[] args)
	    {
	        int number = 1;

	        while (true)
	        {
	            System.out.println("Current number: " + number);
	            number++;

	            if (number > 5) 
	            {
	                System.out.println("Breaking the loop now.");
	                break;
	            }
	        }
	    }
}
	
