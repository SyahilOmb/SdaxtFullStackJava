package fiftyprog.java;

public class seventyth
{
	
	
	    public static void main(String args[])
	    {
	        int i,j,a=5;
	        for(i=1;i<=a;i++)
	        {
	            for(j=0;j<=a;j++)
	            {
	                if(i==0 || i==a || j==0)
	                {
	                    System.out.print("*");
	                }
	                else
	                {
	                    System.out.print(" ");
	  
	                }
	            }
	            System.out.println( );
	        }
	        
	        }
	    

}
